package control_statment;

public class if_else {

    public static void main(String[] args) {

        int number = 3;

        if (number == 1) {
            System.out.print("One");
        } else if (number == 2) {
            System.out.print("Two");
        } else if (number == 3) {
            System.out.print("Three");
        } else if (number == 4) {
            System.out.print("Four");
        } else System.out.print("Invalid number");
    }
}
