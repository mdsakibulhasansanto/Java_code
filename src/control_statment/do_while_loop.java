package control_statment;

public class do_while_loop {

    public static void main(String[] args) {

        // While loop
        int x = 0;
        while (x < 20) {
            System.out.println(x);
            x++;
        }

        // do while loop
        int i = 0;
        do {
            System.out.println("Santo");
            i++;
        } while (i < 10);
    }
}
