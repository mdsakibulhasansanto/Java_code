package data_type;

public class boolean_ {
    public static void main(String[] args) {
        boolean male = false;

        if (male == true){
            System.out.println("Person");
        }

        else if (male == false){
            System.out.println("Girls");
        }
    }

    /*

boolean (সংক্ষেপে bool) ডেটা টাইপ প্রোগ্রামিং ভাষায় সত্য (true) বা মিথ্যা (false) মান ধারণ করার জন্য ব্যবহৃত হয়।
এটি সাধারণত লজিক্যাল বা শর্তমূলক কাজের জন্য ব্যবহৃত হয়,
যেমন কোনো শর্ত সত্য কিনা তা যাচাই করা।
     */
}
